package com.kaugirls.dalal.theedc;


public class InfoWindowData {
    public String shelter_name = "";
    public String shelter_type = "";
    public String shelter_status = "";
    public String shelter_capacity = "";
    public String shelter_description = "";
    public String shelterorAssemblty = "";
    public String shelter_Location = "";

    public InfoWindowData(String shelter_name, String shelter_type, String shelter_status, String shelter_capacity, String shelter_description, String shelterorAssemblty, String shelter_Location) {

        this.shelter_name = shelter_name;
        this.shelter_type = shelter_type;
        this.shelter_status = shelter_status;
        this.shelter_capacity = shelter_capacity;
        this.shelter_description = shelter_description;
        this.shelterorAssemblty = shelterorAssemblty;
        this.shelter_Location = shelter_Location;
    }


}